def build_system_msg():
    return (
        "你是一个写作助手。你必须在输出中每约30个字插入一个标记⟪WD:N⟫，"
        "其中N是从1开始严格递增的整数。例如：...文字... ⟪WD:1⟫ ...文字... ⟪WD:2⟫ ...文字... ⟪WD:3⟫。"
        "N必须是数字（1, 2, 3, ...），不是字母N。这是强制要求。"
    )

def main():
    OUTPUT.mkdir(exist_ok=True)
    system = build_system_msg()
    examples = []

    for user, raw in WRITING_SAMPLES:
        tagged = insert_tags(raw)
        examples.append({
            "messages": [
                {"role": "system", "content": system},
                {"role": "user", "content": user},
                {"role": "assistant", "content": tagged},
            ]
        })

    # 数据增强 3x
    augmented = examples * 3
    random.seed(42)
    random.shuffle(augmented)

    split = int(len(augmented) * 0.85)
    train = augmented[:split]
    valid = augmented[split:]

    for path, data in [(OUTPUT / "train.jsonl", train), (OUTPUT / "valid.jsonl", valid)]:
        with open(path, "w", encoding="utf-8") as f:
            for ex in data:
                f.write(json.dumps(ex, ensure_ascii=False) + "\n")

    print(f"训练集: {len(train)} 条 → {OUTPUT / 'train.jsonl'}")
    print(f"验证集: {len(valid)} 条 → {OUTPUT / 'valid.jsonl'}")
    print(f"\n训练主题: 烹饪/体育/历史/音乐/园艺/天文/茶叶")
    print(f"测试主题: 量子计算/机器学习/小说/重复任务 ← 零重叠")

if __name__ == "__main__":
    main()
