def insert_tags(text, k=K):
    chars = list(text)
    result = []
    count = 0
    tag_n = 1
    for ch in chars:
        result.append(ch)
        if ch not in (" ", "\n", "，", "。", "！", "？", "：", "；", "、", """, """):
            count += 1
        if count >= k:
            result.append(f" ⟪WD:{tag_n}⟫ ")
            tag_n += 1
            count = 0
    return "".join(result)

